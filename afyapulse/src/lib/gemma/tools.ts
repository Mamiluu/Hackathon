export async function executeTool(name: string, args: Record<string, unknown>): Promise<Record<string, unknown>> {
  switch (name) {
    case "list_at_risk_facilities": {
      const snapshots = getAllSnapshots().filter((s) => s.stockRisk !== "ok" || s.alerts.length > 0);
      return {
        facilities: snapshots.map((s) => ({
          name: s.facility.name,
          ward: s.facility.ward,
          healthScore: s.healthScore,
          stockRisk: s.stockRisk,
          topAlert: s.alerts[0]?.message ?? null,
        })),
      };
    }

    case "get_facility_status": {
      const facility = findFacility(String(args.facilityName ?? ""));
      if (!facility) return { error: `No facility found matching "${args.facilityName}"` };
      const snapshot = computeFacilitySnapshot(facility.id);
      return {
        facility: facility.name,
        ward: facility.ward,
        healthScore: snapshot.healthScore,
        stockRisk: snapshot.stockRisk,
        bedOccupancyPct: snapshot.bedOccupancyPct,
        doctorAttendancePct: snapshot.doctorAttendancePct,
        footfallToday: snapshot.footfallToday,
        testKitsMissing: snapshot.testKitsMissing,
        alerts: snapshot.alerts.map((a) => a.message),
      };
    }

    case "get_forecast": {
      const facility = findFacility(String(args.facilityName ?? ""));
      const item = findItem(String(args.itemName ?? ""));
      if (!facility) return { error: `No facility found matching "${args.facilityName}"` };
      if (!item) return { error: `No item found matching "${args.itemName}"` };
      const history = getStockHistory(facility.id, item.id);
      const result = await fetchForecast(history, 14);
      const daysRemainingNow = daysOfStockRemaining(facility.id, item.id);
      return {
        facility: facility.name,
        item: item.name,
        daysRemainingNow: Number.isFinite(daysRemainingNow) ? Math.round(daysRemainingNow * 10) / 10 : null,
        daysToStockout: result.daysToStockout,
        method: result.method,
        confidence: result.confidence,
      };
    }

    case "list_redistribution_proposals": {
      const { proposals } = await computeAllProposals();
      return {
        proposals: proposals.slice(0, 10).map((p) => {
          const item = STOCK_ITEMS.find((i) => i.id === p.itemId);
          return {
            item: item?.name,
            quantity: p.quantity,
            urgency: p.urgency,
            estTransitMinutes: p.estTransitMinutes,
            status: p.status,
          };
        }),
      };
    }

    default:
      return { error: `Unknown tool: ${name}` };
  }
}
