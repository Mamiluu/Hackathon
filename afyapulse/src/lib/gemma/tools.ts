import { getAllSnapshots, computeFacilitySnapshot, daysOfStockRemaining } from "@/lib/data/alertEngine";
import { FACILITIES, STOCK_ITEMS, getStockHistory } from "@/lib/data/store";
import { getForecast as fetchForecast } from "@/lib/forecastingClient";
import { computeAllProposals } from "@/lib/redistribution";
import type { FunctionDeclaration } from "./client";

function findFacility(name: string) {
  const needle = name.trim().toLowerCase();
  return (
    FACILITIES.find((f) => f.name.toLowerCase() === needle) ??
    FACILITIES.find((f) => f.name.toLowerCase().includes(needle)) ??
    FACILITIES.find((f) => needle.includes(f.ward.toLowerCase()))
  );
}

function findItem(name: string) {
  const needle = name.trim().toLowerCase();
  return (
    STOCK_ITEMS.find((i) => i.name.toLowerCase() === needle) ??
    STOCK_ITEMS.find((i) => i.name.toLowerCase().includes(needle)) ??
    STOCK_ITEMS.find((i) => needle.includes(i.category))
  );
}

